namespace Calc
{
    export class Calculator
    {

         Addition(x:number,y:number):number
        {
            return x+y;
        }
        Subtraction(x:number,y:number):number
        {
            return x-y;
        }
        Multiplication(x:number,y:number):number
        {
            return x*y;
        }
        Division(x:number,y:number):number
        {
            return x/y;
        }
        Modulo(x:number,y:number)
        {
            return x%y
        }
    



    }

}